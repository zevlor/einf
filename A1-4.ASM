CLO 			
  JMP START		
  DB 0	
  DB 0		

START:
  MOV AL, 0		
  MOV [03], AL
  MOV BL, 0
  MOV [04], BL	

IF:
  CMP AL, 6			              
  JS INCREMENT
  JMP END

INCREMENT:
  INC AL
  MOV [03], AL
  JMP SECOND_IF

SECOND_IF:
  CMP AL, 3
  JZ INCREMENT_2
  JMP IF

INCREMENT_2:
  ADD BL, 3
  MOV [04], BL
  JMP IF

END:
  MOV AL, [03]
  MOV BL, [04]
  ADD AL, 30
  ADD BL, 30
  MOV [C0], AL 
  MOV [C1], BL
  END
